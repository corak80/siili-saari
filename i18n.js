// ===== Apply translation =====
function applyLang(lang) {
  if (!I18N[lang]) lang = 'fi';
  const dict = I18N[lang];
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = dict[key];
    if (val == null) return;
    const attr = el.getAttribute('data-i18n-attr');
    if (attr) {
      el.setAttribute(attr, val);
    } else if (el.getAttribute('data-i18n-html') === 'true') {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });
  if (dict['meta.title']) {
    // Use privacy.meta.title on privacy page (detect by presence of privacy.title element)
    const isPrivacy = document.querySelector('[data-i18n="privacy.title"]');
    document.title = isPrivacy && dict['privacy.meta.title'] ? dict['privacy.meta.title'] : dict['meta.title'];
  }
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
  try { localStorage.setItem('ss-lang', lang); } catch (e) {}
}

function detectInitialLang() {
  try {
    const saved = localStorage.getItem('ss-lang');
    if (saved && I18N[saved]) return saved;
  } catch (e) {}
  const browser = (navigator.language || 'fi').slice(0, 2).toLowerCase();
  if (I18N[browser]) return browser;
  return 'fi';
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => applyLang(btn.getAttribute('data-lang')));
});

applyLang(detectInitialLang());
